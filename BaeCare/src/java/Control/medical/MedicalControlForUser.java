    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void processGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        HttpSession s = request.getSession();
        User u = (User) s.getAttribute("account");

        String user_email = u.getEmail();

        MedicinePrescriptDAO mpDao = new MedicinePrescriptDAO();
        List<MedicinePrescript> mpList = mpDao.getAllMedicinePrescriptsOfUser(user_email);
        List<Order> listOrderedOfUser = new OrderDAO().getListOrderByEmail(user_email);


       

        request.setAttribute("listOrder", listOrderedOfUser);
        request.setAttribute("medicinePresciptList", mpList);
        request.getRequestDispatcher(viewPath).forward(request, response);

    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void processPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

